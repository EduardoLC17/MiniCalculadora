public class miniCalculadoraEjemplo {
    /**
     * Calcula la raíz cuadrada de un número.
     *
     * @author Eduardo
     * @version 1.0
     * @since 1.0
     * @param numero El número del que se quiere obtener la raíz cuadrada.
     * @return La raíz cuadrada del número proporcionado.
     * @see Math#sqrt(double)
     */
    public static double raizCuadrada(double numero) {

        return Math.sqrt(numero);
    }

    /**
     * Calcula el valor absoluto de un número.
     *
     * @author Eduardo
     * @version 1.0
     * @since 1.0
     * @param numero El número del que se quiere obtener el valor absoluto.
     * @return El valor absoluto del número proporcionado.
     * @see Math#abs(double)
     */
    public static double valorAbsoluto(double numero) {
        return Math.abs(numero);
    }

}
